import java.util.*;
public class FlippedNo {
    public static void main(String[] args) {
        
    
try (Scanner sc = new Scanner(System.in)) {
    int n=sc.nextInt();
    int a =~n;
    //for compliment we use NOT operator
    
     System.out.println("Original no:"+Integer.toBinaryString(n));
     System.out.println("compliment no:"+Integer.toBinaryString(a));
    //for display binary representation we take above syntax
}

    }
}
